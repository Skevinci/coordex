_WUJI_HAND_ACTION_SCALE = 0.1

G1_WUJI_ACTION_SCALE = {}
for name, actuator in G1_WUJI_CFG.actuators.items():
    if name == "wuji_hands":
        for n in actuator.joint_names_expr:
            G1_WUJI_ACTION_SCALE[n] = _WUJI_HAND_ACTION_SCALE
        continue

    eff = actuator.effort_limit_sim
    stiff = actuator.stiffness
    names = actuator.joint_names_expr
    if not isinstance(eff, dict):
        eff = {n: eff for n in names}
    if not isinstance(stiff, dict):
        stiff = {n: stiff for n in names}
    for n in names:
        if n in eff and n in stiff and stiff[n]:
            G1_WUJI_ACTION_SCALE[n] = 0.25 * eff[n] / stiff[n]


